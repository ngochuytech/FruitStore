package Model;

public class CategoryModel {
	private int id;
	private String CategoryName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return CategoryName;
	}
	public void setName(String name) {
		this.CategoryName = name;
	}
}
